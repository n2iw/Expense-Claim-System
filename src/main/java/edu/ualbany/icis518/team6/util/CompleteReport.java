package edu.ualbany.icis518.team6.util;

public class CompleteReport {
	private String employeeName;
	private String projectName;
	private String managerName;
	private int tripDescription;
	private int totalExpendiure;
	
	public String getEmployeeName() {
		return employeeName;
	}
	public void setEmployeeName(String employeeName) {
		this.employeeName = employeeName;
	}
	public String getProjectName() {
		return projectName;
	}
	public void setProjectName(String projectName) {
		this.projectName = projectName;
	}
	public String getManagerName() {
		return managerName;
	}
	public void setManagerName(String managerName) {
		this.managerName = managerName;
	}
	public int getTripDescription() {
		return tripDescription;
	}
	public void setTripDescription(int tripDescription) {
		this.tripDescription = tripDescription;
	}
	public int getTotalExpendiure() {
		return totalExpendiure;
	}
	public void setTotalExpendiure(int totalExpendiure) {
		this.totalExpendiure = totalExpendiure;
	}
	
	

}
